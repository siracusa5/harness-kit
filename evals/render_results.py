#!/usr/bin/env python3
"""Render eval results JSON to the Docusaurus compatibility page."""

import json
import sys
from collections import defaultdict
from datetime import datetime
from pathlib import Path

REPO_ROOT = Path(__file__).parent.parent
RESULTS_FILE = REPO_ROOT / "evals" / "results" / "latest.json"
OUTPUT_DIR = REPO_ROOT / "website" / "docs" / "evals"
OUTPUT_FILE = OUTPUT_DIR / "compatibility.md"

MODEL_ORDER = ["sonnet", "haiku", "opus"]


def pct(score: float) -> str:
    return f"{score * 100:.0f}%"


def render(data: dict) -> str:
    results = data.get("task_results", [])
    run_at = data.get("run_at", "")
    try:
        dt = datetime.fromisoformat(run_at)
        run_at_str = dt.strftime("%Y-%m-%d")
    except Exception:
        run_at_str = run_at or "unknown"

    # Aggregate pass_at_1 by skill + model
    skill_model: dict[str, dict[str, list[float]]] = defaultdict(lambda: defaultdict(list))
    models_seen: set[str] = set()
    for r in results:
        skill_model[r["skill"]][r["model"]].append(r.get("pass_at_1", 0.0))
        models_seen.add(r["model"])

    skills = sorted(skill_model.keys())
    models = [m for m in MODEL_ORDER if m in models_seen]

    lines = [
        "---",
        'sidebar_label: "Compatibility"',
        "---",
        "",
        "# Skill Compatibility Matrix",
        "",
        f"Last updated: {run_at_str}",
        "",
        (
            "> Scores are **pass@1** — fraction of trials that fully pass all structure checks. "
            "Higher is more reliable across runs."
        ),
        "",
        "## Summary",
        "",
    ]

    # Matrix table
    header_cols = " | ".join(m.capitalize() for m in models)
    sep_cols = "|".join("--------" for _ in models)
    lines.append(f"| Skill | {header_cols} |")
    lines.append(f"|-------|{sep_cols}|")
    for skill in skills:
        cells = []
        for m in models:
            scores = skill_model[skill].get(m, [])
            avg = sum(scores) / len(scores) if scores else None
            cells.append(pct(avg) if avg is not None else "—")
        lines.append(f"| {skill} | " + " | ".join(cells) + " |")
    lines.append("")

    # Per-skill breakdown
    for skill in skills:
        lines.append(f"## {skill}")
        lines.append("")
        skill_results = [r for r in results if r["skill"] == skill]
        for model in models:
            model_results = [r for r in skill_results if r["model"] == model]
            if not model_results:
                continue
            lines.append(f"### {model.capitalize()}")
            lines.append("")
            lines.append("| Task | pass@1 | pass@N | pass^N | Quality |")
            lines.append("|------|--------|--------|--------|---------|")
            for r in model_results:
                lines.append(
                    f"| {r['task']} "
                    f"| {pct(r.get('pass_at_1', 0))} "
                    f"| {pct(r.get('pass_at_n', 0))} "
                    f"| {pct(r.get('pass_all_n', 0))} "
                    f"| {pct(r.get('weighted_score', 0))} |"
                )
            lines.append("")

    lines += [
        "---",
        "",
        "*Generated by `evals/render_results.py`. "
        "Trigger the [eval workflow](https://github.com/harnessprotocol/harness-kit/actions/workflows/evals.yml) to regenerate.*",
    ]
    return "\n".join(lines)


def main() -> None:
    if not RESULTS_FILE.exists():
        print(f"Results not found at {RESULTS_FILE}.")
        print("Run `python evals/runner.py` first to generate results.")
        sys.exit(1)

    data = json.loads(RESULTS_FILE.read_text())
    markdown = render(data)
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    OUTPUT_FILE.write_text(markdown)
    print(f"Compatibility page written to {OUTPUT_FILE}")


if __name__ == "__main__":
    main()
